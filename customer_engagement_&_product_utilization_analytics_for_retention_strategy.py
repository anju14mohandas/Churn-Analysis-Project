#Salary-Balance Mismatch
#1. Define the "High Salary" & "Low Balance" thresholds.
#Use the top 25% of earners (75th percentile for salary)
salary_threshold = data['EstimatedSalary'].quantile(0.75)
balance_threshold = 1000
#Customers with less than 1000 in the bank

#2. Identify the Mismatch
#To find people whoo earn MORE than the threshold but keep LESS than the balance limit

data['Mismatch'] = (data['EstimatedSalary'] > salary_threshold) & (data['Balance'] < balance_threshold)

#3. Output
mismatch_count = data['Mismatch'].sum()
total_customers = len(data)
mismatch_percentage = (mismatch_count / total_customers) * 100

print("Salary-Balance Mismatch Results")
print(f"High Salary Threshold: {salary_threshold:.2f}")
print(f"Total Number of Customers: {total_customers}")
print(f"Number of Mismatched Customers: {mismatch_count}")
print(f"Percentage of Mismatched Customers: {mismatch_percentage:.2f}%")
print(f"Percentage of total base: {mismatch_count / total_customers * 100:.2f}%")


#$.Visualize
plt.figure(figsize=(16, 6)) # Corrected plt.fig to plt.figure
sns.scatterplot(data=data, x='EstimatedSalary', y='Balance', hue='Mismatch', palette={True:'green', False:'red'}, alpha = 0.4) # Removed quotes from alpha
plt.axvline(x=salary_threshold, color='black', linestyle='--', label = 'High Salary Threshold')
plt.axhline(y=balance_threshold, color='yellow', linestyle='--', label = 'Low Balance Threshold')
plt.title('Detction of Salary-Balance Mismatch (Red Points)')
plt.legend()
plt.show()

