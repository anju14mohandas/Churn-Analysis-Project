"""### ***Task 2. Engagement Classification***"""

import pandas as pd

#Load dataset (assuming it's already defined elsewhere, or needs to be loaded here)
data = pd.read_excel('/content/European Bank.xlsx')

#Engagement Profiles - group customers based on behaviour
#Using "IsActiveMember", "NomOfProducts", "HasCrCard", "HasCrScore" and "Balance" to define the above.

#Create Engagement Profiles

# Initialize the 'EngagementProfile' column with a default value
data['EngagementProfile'] = 'Standard'

#1. Actively engaged customers (Elite): Active + Multiple Products (2+) + Credit Card Holder + High Credit Score (500+)

data.loc[(data['IsActiveMember'] == 1) & (data['NumOfProducts'] >= 2) & (data['HasCrCard'] == 1) & (data['CreditScore'] >= 500), 'EngagementProfile'] = 'Active engaged customers'

#2. Inactive Disengaged Customers: Not active + low product count + Has credit card + low credit score (<500)

data.loc[(data['IsActiveMember'] == 0) & (data['NumOfProducts'] == 1) & (data['HasCrCard'] == 0) & (data['CreditScore'] < 500), 'EngagementProfile'] = 'Inactive Disengaged Customers'

#3. Active low-product customers: Active + Low product count (=2) + Has Credit Card + High Credit Score (500+)

data.loc[(data['IsActiveMember'] == 1) & (data['NumOfProducts'] == 2) & (data['HasCrCard'] == 1) & (data['CreditScore'] >= 500), 'EngagementProfile'] = 'Active but low-product customers'

#4. Inactive high-balance customers

data.loc[(data['IsActiveMember'] == 0) & (data['Balance'] > 100000), 'EngagementProfile'] = 'Inactive high-balance customers'

#Print the distribution

print(data['EngagementProfile'].value_counts())

#Check if the classification predict churn or not.
#Churn rate (Mean of 'Exited' column); Proportion of customers who have churned/exited the bank

print(data.groupby('EngagementProfile') ['Exited'].mean())

#Visualtisation (Customer Volume by Engagement Profile)

